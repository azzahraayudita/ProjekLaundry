public class Customer {
  private String nama;
  private int telp;
  private String alamat;

  public void Customer (String newNama){
    this.nama=newNama;
  }
  public void setTelp (int newTelp){
    this.telp=newTelp;
  }
  public void setAlamat (String newAlamat){
    this.alamat=newAlamat;
  }
  public void getNama (String newNama){
    this.nama=newNama;
  }
  public void getTelp (int newTelp){
    this.telp=newTelp;
  }
  public void getAlamat(String newAlamat){
    this.alamat=newAlamat;
  }
}
